# While loop
condition = 1
# a loop which runs uptil value of variable condition is less than 10:
while condition < 10:
	print(condition) # print value
	condition += 1  # increment value
# this loop will print numbers from 1 to 10.    

# create a list of integers:
exampleList = [1,5,6,6,2,1,5,2,1,4]
# print the elements in the list using a for-loop:
for x in exampleList:
    print(x)
# print elements 1 to (n-1) using a range-based for loop with range(1,n):
for x in range(1,11):
    print(x)

